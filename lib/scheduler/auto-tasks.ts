export type AutoAgentType = Exclude<AgentType, "finance">;

/**
 * 특정 에이전트 타입에서 무작위 태스크 하나 반환
 */
export function pickRandomTask(agentType: AutoAgentType): string {
  const tasks = AUTO_TASKS[agentType];
  return tasks[Math.floor(Math.random() * tasks.length)];
}

/**
 * 한국 현재 시간 기준 태스크 선택 (시간대별 다른 태스크)
 */
export function pickScheduledTask(agentType: AutoAgentType): string {
  const hour = new Date().getHours();
  const tasks = AUTO_TASKS[agentType];
  // 시간대에 따라 다른 인덱스 (하루 최대 5번 실행 시 순환)
  const idx = Math.floor(hour / 5) % tasks.length;
  return tasks[idx];
}
